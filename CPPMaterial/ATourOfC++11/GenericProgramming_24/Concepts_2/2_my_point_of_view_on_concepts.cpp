/**
* In mathematics, and particularly in algebraic structures, we define a set of conditions (or axioms), and if a
* set of objects satisfy those constraints, then we have a particular structure. Same can be said about concepts.
* 
* The point here is that we don't have thousands upon thousands of structures in algebra, and neither do we here in
* template concepts. For example we have a regular type concept, and couple more, and as the big guy says, it in
* general takes a year or so to discover a new concept!
* 
* Consequently, we shouldn't play fast an loose with concepts. On the other hand a type satisfying a concept does
* not necessarily guarantee that it will comply with the operations we define. Same as for example saying that a
* matrix satisfies vector space constraints, but it's not necessarily convertible.
*/